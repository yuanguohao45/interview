// translate 是 transform 的属性值，是指元素进行 2D(3D)维度上位移或范围变换;
// transition 是指过渡效果，往往理解成简单的动画，需要有触发条件。

// 这里可以补充下 transition 和 animation 的比较，前者一般定义开始结束两个状态，需要有触发条件；而后者引入了关键帧、速度曲线、播放次数等概念，更符合动画的定义，且无需触发条件

